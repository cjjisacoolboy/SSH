package com.icss.pojo;

public class CarbodyInfo {
	private int carbodyId;
	private double trunkVolume;
	private double tankVolume;
	private double kerbMass;
	private double minimumGround;
	private double wheelbase;
	private String bodyStructure;
	
	
	public int getCarbodyId() {
		return carbodyId;
	}
	public void setCarbodyId(int carbodyId) {
		this.carbodyId = carbodyId;
	}
	public double getTrunkVolume() {
		return trunkVolume;
	}
	public void setTrunkVolume(double trunkVolume) {
		this.trunkVolume = trunkVolume;
	}
	public double getTankVolume() {
		return tankVolume;
	}
	public void setTankVolume(double tankVolume) {
		this.tankVolume = tankVolume;
	}
	public double getKerbMass() {
		return kerbMass;
	}
	public void setKerbMass(double kerbMass) {
		this.kerbMass = kerbMass;
	}
	public double getMinimumGround() {
		return minimumGround;
	}
	public void setMinimumGround(double minimumGround) {
		this.minimumGround = minimumGround;
	}
	public double getWheelbase() {
		return wheelbase;
	}
	public void setWheelbase(double wheelbase) {
		this.wheelbase = wheelbase;
	}
	public String getBodyStructure() {
		return bodyStructure;
	}
	public void setBodyStructure(String bodyStructure) {
		this.bodyStructure = bodyStructure;
	}
	
}
