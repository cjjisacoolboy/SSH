package com.icss.pojo;

public class ParametersConfig {
	private Integer parametersConfigId;
	private String transmission;
	private String carColor;
	private String envStandards;
	private String fuel;
	private String drivingMode;
	private String manufacturer;
	public Integer getParametersConfigId() {
		return parametersConfigId;
	}
	public void setParametersConfigId(Integer parametersConfigId) {
		this.parametersConfigId = parametersConfigId;
	}
	public String getTransmission() {
		return transmission;
	}
	public void setTransmission(String transmission) {
		this.transmission = transmission;
	}
	public String getCarColor() {
		return carColor;
	}
	public void setCarColor(String carColor) {
		this.carColor = carColor;
	}
	public String getEnvStandards() {
		return envStandards;
	}
	public void setEnvStandards(String envStandards) {
		this.envStandards = envStandards;
	}
	public String getFuel() {
		return fuel;
	}
	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
	public String getDrivingMode() {
		return drivingMode;
	}
	public void setDrivingMode(String drivingMode) {
		this.drivingMode = drivingMode;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	
}
